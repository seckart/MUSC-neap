	<h1 class="title">
		Magnet<sup>&reg;</sup> Data Requirements
	</h1>
	
	<p>
		Data must be sourced from an ANCC<sup>&reg;</sup>-approved database/vendor, and benchmarked against a national database, at the unit or clinic level, within the preceding 30 months.
	</p>

	<p>
		Within each measure, a majority of units must outperform the national benchmark for at least 5 of the last 8 quarters.
	</p>

	<ul>

		<li>
			RN Satisfaction: must include questions representing 4 Magnet-approved categories from approved survey.
		</li>
		<li>
			Nurse-Sensitive Indicators: 8 quarters of data for inpatient, pediatric, and ambulatory/outpatient units; CAUTI rate per 1000 Catheter Days, CLABSI rate per 1000 Central Line Days, Injury Falls per 1000 Patient Days/Visits, and HAPU Stage 2+ per 100 Patients; both hospital and benchmark outcomes by quarters.
		</li>
		<li>
			Patient Satisfaction: 8 quarters of data for each unit/survey; both hospital and benchmark results by quarter; questions approved for Magnet submission.
		</li>

	</ul>

