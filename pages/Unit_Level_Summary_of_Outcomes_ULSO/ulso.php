	<p>
		The Unit-Level Summary of Outcomes (“ULSO”) is a foundational document for all Nursing Excellence clients who partner with HealthLinx<sup>&reg;</sup>.
	</p>

	<p>
		This tool effectively aligns, measures and evaluates the most recent eight (8) quarters of nationally benchmarked data, which includes Patient Satisfaction data, RN Satisfaction data, and the Nurse-Sensitive Indicators (“NSI”), both horizontally and vertically.
	</p>

	<p>
		In addition, the ULSO allows to measure whether or not 50% of a facility’s units are outperforming the benchmark more than 50% of the time, in the most recent eight (8) quarters.
	</p>

	<img src="<?php echo $TEMPLDIR; ?>/ulso.png" alt="" />

	<p>
		The ULSO is completed initially during the Nursing Excellence Assessment and Plan (NEAP). 
	</p>

	<p>
		Completing the ULSO early in process allows for quarterly updating of data to continually measure, recalibrate and affect the organizational readiness to pursue Magnet<sup>&reg;</sup> Designation, but more importantly, to establish a sustainable culture of nursing excellence.
	</p>

	<p>
		Without both facility-wide or cultural (Vertical) performance, leadership will struggle. Without strong high performing leadership on all units (Horizontal), the overall facility, culture and strategic vision will underperform. The Vertical-Horizontal perspective provided by the ULSO illustrates the symbiotic relationship of the two.
	</p>

