	<h1 class="title">
		CMS Incentives FAQ
	</h1>

	<p>
		CMS provides three outcomes-based financial incentives to hospitals. They are:
	</p>

	<ol>
		<li>Value-Based Purchasing incentive</li>
		<li>Readmissions reduction</li>
		<li>Hospital-Acquired Conditions reduction</li>
	</ol>

	<p>
		The three are collectively referred to as CMS Incentives. CMS refers to negative amounts as "reductions", but in plainer terms they are penalties and will be referred to as such by HealthLinx<sup>&reg;</sup>. For FY2016, the incentive ranges from a total penalty of 5.75% to a total bonus of 1.75%. Therefore the total "swing range" for FY2016 is 7.5%. For FY2017, this swing range increases to 8% and will stay there under current rules. This bonus/penalty hits every year.
	</p>

	<p>
		Data is pulled from CMS's data portal site and from other private and proprietary data sources. The penalty/bonus percentagesare typically available to the public. The dollar amounts for penalty/bonus and opportunity are calculated from non-public sources and are thus estimates.
	</p>

	<div class="bold">Value-Based Purchasing (VBP)</div>

	<p>
		A percentage is withheld from a hospital's total Medicare payments: 1.5% for FY2015, 1.75% for FY2016 and 2% for FY2017 and beyond. This withholding goes into a pool that all participating hospitals have the opportunity to access based on their outcomes (performance).
		<br />
		<span class="underline">The rest of this section uses FY2016 in all examples.</span>
	</p>

	<p>
		Roughly speaking, hospitals performing right at the national average will earn back their withheld 1.75% for a net effect of 0%. Those performing above average will receive back less than 1.75% plus a bonus corresponding to their rank above average, up to an additional 1.75%. Those performing below average will receive back less than 1.75% back(effectively a penalty).
	</p>

	<p>
		For the sake of consistency, HealthLinx<sup>&reg;</sup> follows CMS and other industry standards. That is, an average hospital has a VBP % of zero. A below average hospital has a VBP % below zero (i.e., a VBP penalty) and an above average hospital has a VBP % above zero (i.e., a VBP bonus). To further clarify, a bottom performing hospital would have a VBP % of -1.75% while a top performing hospital would have a VBP % of 1.75%.
	</p>

	<p>
		NOTE: Due to certain adjustment factors and the nature of the math used to generate the score distribution, every year there are typically a few hospitals that exceed the maximum bonus percentage.
	</p>

	<p class="italic">
		Example
	</p>

	<p>
		Hospital A has $100 million in total Medicare payments. It has amazing outcomes and therefore has the maximum possible VBP % of 1.75%. Not only do they get back their 1.75% to "break even", they get an extra 1.75% bonus worth $1.75 million. This hospital has $0 of VBP opportunity.
	</p>

	<p>
		Hospital B has $100 million in total Medicare payments. It has terrible outcomes and therefore has the lowest possible VBP % of -1.75%. They not only don't break even, they effectively pay a penalty of $1.75 million. This hospital has $3.5 million of VBP opportunity: the -$1.75 million penalty they actually paid plus the $1.75 million bonus they failed to earn.
	</p>

	<div class="bold">Readmissions Reduction</div>

	<p>
		Hospitals with poor readmissions performance can receive a reduction (effectively a penalty) of up to 3%. Logos treats a penalty as a negative number, so a hospital can have a readmissions % from 0% (best) to -3% (worst).
	</p>

	<p class="italic">
		Example
	</p>

	<p>
		Hospital A has $100 million in total Medicare payments. It has amazing outcomes and therefore has the best possible readmission penalty of 0%. They pay $0 in readmission penalty. This hospital has $0 of readmission opportunity.
	</p>

	<p>
		Hospital B has $100 million in total Medicare payments. It has the worst possible readmission penalty of -3%. They pay $3 million in readmission penalty. This hospital has $3 million of readmission opportunity.
	</p>
	
	<div class="bold">Hospital-Acquired Conditions (HAC) Reduction</div>

	<p>
		Hospitals with poor HAC performance (in the bottom quartile) receive a reduction (effectively a penalty) of precisely 1%. All others receive no reduction. Logos treats a penalty as a negative number, so a hospital can have a HAC penalty of either 0% (best) or -1% (worst). Unlike other incentives, this one is all or nothing.
	</p>

	<p class="italic">
		Example
	</p>

	<p>
		Hospital A has $100 million in total Medicare payments. Is has amazing outcomes and therefore has a HAC penalty of 0%. They pay $0 in HAC penalty. This hospital has $0 of HAC opportunity.
	</p>

	<p>
		Hospital B has $100 million in total Medicare payments. It has terrible outcomes and therefore has the HAC penalty of -1%. They pay $1 million in HAC penalty. This hospital has $1 million of HAC opportunity.
	</p>

