	<h1 class="title">Evaluating Hospitals</h1>

	<p>
		CMS assesses each hospital's total performance by comparing its achievement and improvement scores for each applicable hospital Value-Based Purchasing (VBP) measure and awarding the higher score for each measure. CMS then aggregates each hospital's scores into the appropriate domain. The FY 2016 Hospital VBP Program consists of four domains:
	</p>

	<ol>
		<li>Clinical Process of Care (10% domain weight)</li>
		<li>Patient Experience of Care (25% domain weight)</li>
		<li>Efficiency (25% domain weight)</li>
		<li>Outcome (40% domain weight)</li>
	</ol>
	
	<p>
		After each domain score is multiplied by its percentage value, CMS adds the weighted domain scores to reach a hospital's Top Performance Score.
	</p>

	<h4 class="section-header">Total Performance Score</h4>

	<p>
		Each hospital's Total Performance Score is converted into a Value-Based Incentive payment adjustment percentage using a mathematical formula. In FY 2016, CMS uses incentive payment adjustment percentages from -1.75% to +1.75%.
	</p>

