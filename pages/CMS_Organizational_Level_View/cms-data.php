	<h1 class="title">CMS Data</h1>

	<p>
		CMS data focuses on organizational-level performance and the vast majority of data required by the ANCC Magnet Recognition Program<sup>&reg;</sup> is unit specific. However, for the following reasons we will include CMS data:
	</p>

	<ul>
		<li>It is another data sources similar to the Vertical Analysis (Hospital-Wide) provided in this Assessment; for example, it offers another view on patients' opinion of a facility.</li>
		<li>It provides a convenient External Vertical comparison (other hospitals) in a hospital's local and competitive markets.</li>
		<li>Utilizing "Outcomes" and "Timely &amp; Effective Care", it can provide additional, valuable unit-level information.</li>
		<li>Again, utilizing "Outcomes" and "Timely &amp; Effective Care", it can provide convenient External Horizontal comparison (other hospitals, unit- and leader-specific) of a hospital's units to other hospitals' like units.</li>
	</ul>
	
	<p>
		CMS is changing. As of CMS has introduced a new rating which is the overall hospital star rating. This new rating uses up to 64 measures to rank how a hospital performs relative to a national average. Seven primary areas factor into the rating: mortality, safety of care, readmissions, patient experience, effectiveness of care, timeliness of care and effective use of medical imaging. The overall hospital rating scale is 1-5 stars, with 3 stars being considered average (around 40% of facilities receive this).
	</p>

	

